package com.example.apppetrobras.Objects;

public class Infos {
    public String nome;
    public String email;
    public String tel;
    public String dataNasc;
    public String chave;
    public String senha;


    public Infos(String _nome, String _email, String _tel, String _dataNasc, String _chave, String _senha){
        this.nome = _nome;
        this.email = _email;
        this.tel = _tel;
        this.dataNasc = _dataNasc;
        this.chave = _chave;
        this.senha = _senha;

    }
}
